    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

       if (formValid()) {
            try {
                Actor article = new Actor(
                        tfFirstName.getText().trim(),
                        tfLastName.getText().trim()
                );
                repository.createActor(article);
                actorTableModel.setActors(repository.selectActors());

                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(EditActorsPanel.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to create article!");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

